if gpa >= 90:
  print("Your grade is  S" ) 
elif gpa >=80:
  print("Your grade is A ")
elif gpa >=70:
  print("Your grade is  B" )
elif gpa >=60:
  print("Your grade is  C")
elif gpa >=50:
  print("Your grade is  D")
elif gpa >= 40:
  print("Your grade is  E")
elif gpa >= 35:
  print("Your grade is  P")
else:
  print("Your grade is  Fail ")